                <h2>Inicios</h2>
                <p>David Valls (Alicante, 1994) comienza a la edad de 13 años sus primeros pasos en el mundo de la interpretación dentro 
                    de los talleres escénicos de la compañía alicantina Almadraba Teatro, 
                    dirigido por Jose Luis Alonso. Realiza cuatro montajes teatrales 
                    consecutivos a cargo de Mila García y Gloria Sirvent a la vez que 
                    termina sus estudios de Bachillerato para entrar a formarse en 2013 a 
                    la Escuela Superior de Arte Dramático de Murcia, en Interpretación musical.</p>
            </article>

            <article>
                <h2>Etapa acad&eacute;mica</h2>
                <p>En la escuela realiza varios montajes a la vez que cursa el Método 
                    del Actors Studio con César Oliva Bernal, Técnica Meisner con Jose Antonio 
                    Sánchez, Técnica Vocal con Carmen Acosta e Interpretación en el Musical con 
                    Silvia Montesinos, entre otras muchas materias. Allí también obtiene el nivel 
                    1 y 2 de Still Voice Training, impartido por Robert Sussuma. En tercer curso viaja a Londres
                    para asistir a un Workshop de Teatro Musical en 
                    Regent´s University.</p>
                            
                <p>Tras cuatro años de grado, finaliza sus estudios en la ESAD protagonizando el 
                    musical Company en el Teatro Circo de Murcia.</p>
            </article>

            <article>
                <h2>Madrid</h2>
                <p>Seguidamente se traslada a Madrid para participar en el taller Realidad/Deseo de La 
                    Joven Compañía, para posteriormente continuar sus entrenamientos en ella con Andoni Larrabeiti.</p>
            
                <p>En 2019 realiza el Programa Avanzado de Interpretación ante la Cámara, en la Central de Cine, 
                    formándose con Eva Lesmes, Juancho Calvo, Juan León, Álvaro Haro, Natalia Mateo y Sonia Baena.</p>
            
                <p>Ya en Madrid se ha involucrado tanto en diversas obras teatrales como <i>Roberto Zucco</i>, dirigida por Pedro Estévez; 
                <i>Mierda Bonita</i>, dirigida por Javier Aranzadi y el micromusical <i>Nombre del Musical</i>, dirigida por Víctor Páez. 
                    En el audiovisual participa en el documental <i>La Vida en el Campo</i> y protagoniza el cortometraje <i>I Love You</i>, <i>Olvido</i>, 
                    <i>Controversia</i> y el piloto de la webserie <i>Operación Presidente</i>.</p>
